// caseAgents.js — Stage automation engine for case management
// Triggers actions (letter generation, deadlines, record requests) when a case moves stages.

const API_BASE = "https://tools.sherlawgroup.com";

// ─── Helpers ─────────────────────────────────────────────────

/**
 * Authenticated fetch to the backend API.
 */
async function agentApiFetch(path, options = {}) {
  const token = typeof getIdToken === "function" ? await getIdToken() : null;
  const url = path.startsWith("http") ? path : `${API_BASE}${path}`;
  const resp = await fetch(url, {
    ...options,
    headers: {
      "Content-Type": "application/json",
      ...(token ? { Authorization: `Bearer ${token}` } : {}),
      ...options.headers,
    },
  });
  if (!resp.ok) {
    const body = await resp.text();
    throw new Error(`API ${resp.status}: ${body}`);
  }
  if (resp.status === 204) return null;
  return resp.json();
}

/**
 * Call Claude API to generate a document draft.
 * Returns the text content of the response.
 */
async function generateWithClaude(systemPrompt, userMessage) {
  const apiKey =
    typeof getClaudeApiKey === "function" ? getClaudeApiKey() : null;
  if (!apiKey) throw new Error("Claude API key not configured");

  const resp = await fetch("https://api.anthropic.com/v1/messages", {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "x-api-key": apiKey,
      "anthropic-version": "2023-06-01",
      "anthropic-dangerous-direct-browser-access": "true",
    },
    body: JSON.stringify({
      model: CLAUDE_MODEL,
      max_tokens: 4096,
      system: systemPrompt,
      messages: [{ role: "user", content: userMessage }],
    }),
  });

  if (!resp.ok) {
    const body = await resp.text();
    throw new Error(`Claude API ${resp.status}: ${body}`);
  }

  const data = await resp.json();
  return data.content?.[0]?.text || "";
}

/**
 * Save a generated document to the backend.
 */
async function saveDocument(caseId, title, content, docType) {
  return agentApiFetch("/api/case-documents", {
    method: "POST",
    body: JSON.stringify({ caseId, title, content, docType }),
  });
}

/**
 * Trigger auto-generated deadlines for a given stage.
 */
async function autoGenerateDeadlines(caseId, stage) {
  return agentApiFetch("/api/deadlines/auto-generate", {
    method: "POST",
    body: JSON.stringify({ caseId, stage }),
  });
}

/**
 * Look up a case by ID from local storage.
 */
function getCase(caseId) {
  const cases = loadCases();
  const c = cases.find((x) => x.id === caseId);
  if (!c) throw new Error(`Case ${caseId} not found`);
  return c;
}

