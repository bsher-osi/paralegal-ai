/**
 * INTAKE — Create OneDrive folder structure for the new case.
 */
async function handleIntake(caseId) {
  const c = getCase(caseId);
  const clientName = c.clientName || c.name || "Unknown Client";
  const caseType = c.caseType || c.type || "General";
  const folderName = `${clientName} - ${caseType}`;

  const ignore409 = (e) => { if (!String(e.message).includes("409") && !String(e.message).includes("nameAlreadyExists")) throw e; };

  const spd = await getSpDrive();

  // Create Cases folder at drive root
  await graphFetch(`${spd}/root/children`, {
    method: "POST",
    body: JSON.stringify({ name: "Cases", folder: {}, "@microsoft.graph.conflictBehavior": "fail" }),
  }).catch(ignore409);

  // Create the case folder inside Cases
  await graphFetch(`${spd}/root:/Cases:/children`, {
    method: "POST",
    body: JSON.stringify({ name: folderName, folder: {}, "@microsoft.graph.conflictBehavior": "fail" }),
  }).catch(ignore409);

  const subfolders = ["Intake", "Correspondence", "Medical Records", "Settlement", "Liens", "Health Insurance", "Police Report", "Demand", "Attachments"];

  await Promise.allSettled(
    subfolders.map((name) =>
      graphFetch(`${spd}/root:/Cases/${folderName}:/children`, {
        method: "POST",
        body: JSON.stringify({ name, folder: {}, "@microsoft.graph.conflictBehavior": "fail" }),
      }).catch(ignore409)
    )
  );

  showToast(`SharePoint folder created: Cases/${escapeHtml(folderName)}`, "success");
}

/**
 * LOR_SENT — Generate Letters of Representation and create deadlines.
 */
async function handleLorSent(caseId) {
  const c = getCase(caseId);
  const clientName = c.clientName || c.name || "Unknown Client";
  const dateOfLoss = c.dateOfIncident || c.dateOfLoss || "N/A";
  const claimNumber = c.claimNumber || "N/A";

  const lorSystemPrompt = `You are a paralegal AI assistant at Sher Law Group, a personal injury law firm in Arizona. Draft a professional Letter of Representation (LOR). The letter should:
- Use Sher Law Group letterhead format (firm name, address placeholder)
- Be addressed from the attorney at Sher Law Group
- State that the firm has been retained to represent the client regarding their personal injury claim
- Reference the date of loss and claim number
- Request all future communications be directed to the firm
- Note that a HIPAA authorization is enclosed for release of medical records and billing
- Include standard Arizona personal injury LOR language
- Request preservation of all evidence including surveillance footage
- Be formal, professional, and concise
- End with a professional closing and signature block`;

  // Letter to at-fault insurance carrier
  const atFaultCarrier = c.insuranceCompany || c.atFaultCarrier || "Unknown Carrier";
  const adjusterName = c.adjusterName || "Claims Department";
  const adjusterInfo = c.adjusterEmail
    ? `${adjusterName} (${c.adjusterEmail})`
    : adjusterName;

  const atFaultPrompt = `Draft a Letter of Representation to the at-fault party's insurance carrier with the following details:

Recipient: ${adjusterInfo}
Insurance Company: ${atFaultCarrier}
Claim Number: ${claimNumber}
Client Name: ${clientName}
Date of Loss: ${dateOfLoss}
Case Type: ${c.caseType || c.type || "Personal Injury"}

This letter is being sent to the at-fault party's insurer. Include language directing all communication to the firm, requesting claim information, and noting the enclosed HIPAA authorization.`;

  // Letter to client's own auto carrier (UM/UIM / MedPay)
  const clientCarrier = c.clientInsuranceCompany || c.clientCarrier || "Client's Auto Carrier";
  const clientPolicyNumber = c.clientPolicyNumber || "N/A";

  const clientCarrierPrompt = `Draft a Letter of Representation to the client's own automobile insurance carrier with the following details:

Recipient: Claims Department
Insurance Company: ${clientCarrier}
Policy Number: ${clientPolicyNumber}
Client Name: ${clientName}
Date of Loss: ${dateOfLoss}
Case Type: ${c.caseType || c.type || "Personal Injury"}

This letter is being sent to the client's own auto carrier to put them on notice of a potential UM/UIM or MedPay claim. Include language directing all communication to the firm, requesting policy declarations and limits information, and noting the enclosed HIPAA authorization.`;

  // Generate both letters in parallel
  const [atFaultLetter, clientCarrierLetter] = await Promise.all([
    generateWithClaude(lorSystemPrompt, atFaultPrompt),
    generateWithClaude(lorSystemPrompt, clientCarrierPrompt),
  ]);

  // Save both letters and create deadlines in parallel
  await Promise.all([
    saveDocument(
      caseId,
      `LOR - ${atFaultCarrier}`,
      atFaultLetter,
      "lor_at_fault"
    ),
    saveDocument(
      caseId,
      `LOR - ${clientCarrier}`,
      clientCarrierLetter,
      "lor_client_carrier"
    ),
    autoGenerateDeadlines(caseId, "lor_sent"),
  ]);

  showToast("Letters of Representation generated and deadlines created", "success");
}

