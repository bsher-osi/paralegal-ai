/**
 * SETTLED — Generate lien reduction letters for providers with outstanding balances.
 */
async function handleSettled(caseId) {
  const c = getCase(caseId);
  const clientName = c.clientName || c.name || "Unknown Client";

  // Fetch specials (medical providers/billing)
  const specials = await agentApiFetch(`/api/specials?caseId=${caseId}`);
  const providers = Array.isArray(specials) ? specials : specials?.items || [];

  // Filter to providers with outstanding balances
  const outstanding = providers.filter(
    (p) => p.outstandingBalance > 0 || p.balance > 0 || p.amountOwed > 0
  );

  if (outstanding.length === 0) {
    showToast("No outstanding provider balances found — no reduction letters needed", "info");
    return;
  }

  const systemPrompt = `You are a paralegal AI assistant at Sher Law Group, a personal injury law firm in Arizona. Draft a professional lien reduction request letter to a medical provider. The letter should:
- Be on Sher Law Group letterhead format
- Reference the client's personal injury case
- State the total medical charges from the provider
- Explain that the case has settled and request a reduction in the outstanding balance
- Note that the reduction will facilitate resolution and timely payment
- Be respectful and professional while advocating for the client
- Include a signature block for the attorney at Sher Law Group
- Request a response within 30 days`;

  // Generate all reduction letters in parallel
  const letterPromises = outstanding.map(async (provider) => {
    const providerName = provider.providerName || provider.name || "Provider";
    const totalCharges =
      provider.totalCharges || provider.billedAmount || provider.amount || 0;
    const outstandingAmt =
      provider.outstandingBalance || provider.balance || provider.amountOwed || 0;

    const userPrompt = `Draft a lien reduction letter for the following:

Client: ${clientName}
Provider: ${providerName}
Total Charges: $${Number(totalCharges).toLocaleString("en-US", { minimumFractionDigits: 2 })}
Outstanding Balance: $${Number(outstandingAmt).toLocaleString("en-US", { minimumFractionDigits: 2 })}
Settlement Status: Case has been settled.

Request a reasonable reduction of the outstanding balance to facilitate prompt payment.`;

    const letter = await generateWithClaude(systemPrompt, userPrompt);

    await saveDocument(
      caseId,
      `Reduction Letter - ${providerName}`,
      letter,
      "reduction_letter"
    );

    return providerName;
  });

  const results = await Promise.allSettled(letterPromises);
  const succeeded = results.filter((r) => r.status === "fulfilled").length;
  const failed = results.filter((r) => r.status === "rejected").length;

  if (failed > 0) {
    showToast(
      `Reduction letters: ${succeeded} generated, ${failed} failed`,
      "warning"
    );
  } else {
    showToast(
      `${succeeded} lien reduction letter(s) generated`,
      "success"
    );
  }
}

