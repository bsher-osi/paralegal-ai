/**
 * Stage-handler dispatch map.
 */
const STAGE_HANDLERS = {
  fee_agreement_sent: handleFeeAgreementSent,
  lor_sent: handleLorSent,
  demand_sent: handleDemandSent,
  litigation_filed: handleLitigationFiled,
  litigation_served: handleLitigationServed,
  litigation_answered: handleLitigationAnswered,
  settled: handleSettled,
};

/**
 * Called whenever a case moves to a new stage.
 * Dispatches to the appropriate handler without blocking the UI.
 *
 * @param {string} caseId
 * @param {string} oldStage
 * @param {string} newStage
 */
function onStageChange(caseId, oldStage, newStage) {
  const handler = STAGE_HANDLERS[newStage];
  if (!handler) return;

  console.log(
    `[caseAgents] Stage change: ${oldStage} → ${newStage} (case ${caseId})`
  );

  // Fire-and-forget — runs async without blocking the caller
  handler(caseId).catch((err) => {
    console.error(`[caseAgents] Error in ${newStage} handler:`, err);
    showToast(`Stage automation error: ${err.message}`, "error");
  });
}

